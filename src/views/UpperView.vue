       */
      watch(
        () => props.activeUpperTab,
        (newTab) => {
          if (newTab === 'grid-scaling') {
            nextTick(() => {
              setTimeout(() => {
                if (GridScalingTab.value && GridScalingTab.value.resize) {
                  GridScalingTab.value.resize();
