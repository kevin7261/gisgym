            });
          }
        }
      );

      /**
       * ğŸ‘€ ç›£è½é¢æ¿å¤§å°è®ŠåŒ– (Watch Panel Size Changes)
       * ç•¶é¢æ¿å¯¬åº¦æˆ–é«˜åº¦è®ŠåŒ–æ™‚ï¼Œæ›´æ–°ç›¸æ‡‰çš„å­çµ„ä»¶
       */
      watch([() => props.mainPanelWidth, () => props.contentHeight], () => {
        nextTick(() => {
          // è§¸ç™¼å„å€‹ Tab é‡æ–°èª¿æ•´ä»¥é©æ‡‰æ–°çš„å®¹å™¨å°ºå¯¸
