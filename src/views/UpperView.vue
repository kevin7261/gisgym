      GridScalingTab,
      ProcessedJsonDataTab,
      JsonDataTab,
      DrawJsonDataTab,
    },

    /**
     * ğŸ”§ çµ„ä»¶å±¬æ€§å®šç¾© (Component Props)
     * æ¥æ”¶ä¾†è‡ªçˆ¶çµ„ä»¶çš„é…ç½®å’Œç‹€æ…‹æ•¸æ“š
